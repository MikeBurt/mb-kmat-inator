export const getOptions = async () => {
	let options = [];
	await getClasses().then((response) => {
		response.e1klahm.e1ksmlm.forEach((x) => {
			options.push({ line: x.posnr, key: x.atnam });
		});

		getCharacteristics().then((response) => {
			options.forEach((c, i) => {
				let f = response.e1cabnm.find((y) => y.atnam === c.key);
				if (f) {
					let allowedValues = [];
					f.e1cawnm.forEach((z) => {
						allowedValues.push({
							value: z.atwrt,
							description: z.e1cawtm.atwtb,
						});
					});

					options[i].description = f.e1cabtm.atbez;
					options[i].allowedValues = allowedValues.sort((a, b) => {
						if (a.description < b.description) {
							return -1;
						} else if (a.description > b.description) {
							return 1;
						} else {
							return 0;
						}
					});
				}
			});
		});
	});
	return options.filter((c) => c.description);
};

